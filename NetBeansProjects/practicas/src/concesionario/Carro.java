package concesionario;

public class Carro {

    public String nombre;
    public String marca;
    public String color;
    public String gama;
    public String tipo;

    public Carro(String nombre, String marca, String color, String gama, String tipo) {
        this.nombre = nombre;
        this.marca = marca;
        this.color = color;
        this.gama = gama;
        this.tipo = tipo;
        
    }

}
